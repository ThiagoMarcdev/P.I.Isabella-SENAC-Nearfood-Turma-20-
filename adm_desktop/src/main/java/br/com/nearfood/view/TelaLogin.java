    private void btnBotaoEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBotaoEntrarActionPerformed
       //funções ao clicar no botão "entrar" na tela de login
       
       
         Usuario user = new Usuario();
        
        //USUÁRIO E SENHA
        String email = txtEmail.getText();
        String senha = txtSenha.getText();
        
        try {
            if (user.login(email, senha)){
                //String nome = user.getNome(email);
                TelaPrincipal home = new TelaPrincipal();
                home.setVisible(true);
                this.dispose();
            }
            else{
                txtMensagem.setForeground(Color.RED);
                txtMensagem.setText("Usuário ou senha incorretos!");
            } 
        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
       
       
         
        
    }//GEN-LAST:event_btnBotaoEntrarActionPerformed

