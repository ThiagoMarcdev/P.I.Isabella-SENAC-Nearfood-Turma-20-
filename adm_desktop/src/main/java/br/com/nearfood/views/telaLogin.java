    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        
        Usuario user = new Usuario();
        
        //USUÁRIO E SENHA
        String cnpj = txtUserName.getText();
        String senha = txtPassword.getText();
        
        try {
            if (user.login(cnpj, senha)){
                String nome = user.getNome(cnpj);
                TelaGerenciamento home = new TelaGerenciamento(nome);
                home.setVisible(true);
                this.dispose();
            }
            else{
                txtMensagem.setForeground(Color.RED);
                txtMensagem.setText("Usuário ou senha incorretos!");
            } 
        } catch (SQLException ex) {
            Logger.getLogger(TelaLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
       
    }                                        

